public class Persona {
    
    private int edad;
    private String estadoCivil;

    public Persona(int edad, String estadoCivil) {
        this.edad = edad;
        this.estadoCivil = estadoCivil;
    }

    public int getEdad() {
        return edad;
    }

    public String getEstadoCivil() {
        return estadoCivil;
}
}